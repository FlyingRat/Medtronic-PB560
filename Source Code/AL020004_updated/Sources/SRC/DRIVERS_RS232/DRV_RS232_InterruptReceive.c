void DRV_RS232_InterruptReceive(void) 
{ 
	/*%C RS232 Buffer is loaded in the buffer of receipt */
	BufferOfReceipt[PtrWriteBufferRx] = RCREG2;
	PtrWriteBufferRx = PtrWriteBufferRx + 1;

	/*%C protection of pointer with modulo of RX_BufferSize */
	PtrWriteBufferRx %= RX_BufferSize;
}
