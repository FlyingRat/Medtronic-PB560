/******************************************************************************/
/*%C        Functionnal description : 	this function reads the info value in the	*/
/*%C         alarm base																			*/
/*                                                            				  		*/
/******************************************************************************/
/*%I 	Input Parameter : 			Id			        			                  	*/
/*%IO Input/Output Parameter :   None                                		  	*/
/*%O 	Output Parameter : 			Alarm info           				  		*/
/******************************************************************************/

/******************************************************************************/
/*                                INCLUDE FILES		                          	*/
/******************************************************************************/
#include "typedef.h"
#include "DB_AlarmStatus.h"
/******************************************************************************/
/*                     EXTERNAL FUNCTION DECLARATION                         	*/
/******************************************************************************/
UBYTE DB_AlarmInfoRead(e_DB_ALARMS Id)
{
/*%C Returns the alarm status array element value function of Id             	*/
	return ((DB_AlarmStatus[Id] & 0xFF00) >> 8);	
}
