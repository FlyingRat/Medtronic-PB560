void DB_AlarmStatusWrite(e_DB_ALARMS Id, e_DB_ALARMS_STATE Value)
{
	// Alarmes non techniques
	if (Id < end_of_alarms_table)
   {
   		Value &= 0x00FF;
		DB_AlarmStatus[Id] &= 0xFF00;
		DB_AlarmStatus[Id] |= Value;
   }
}
