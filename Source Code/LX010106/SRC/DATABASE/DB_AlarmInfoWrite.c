/******************************************************************************/
/*                     EXTERNAL FUNCTION DECLARATION                         	*/
/******************************************************************************/
void DB_AlarmInfoWrite(e_DB_ALARMS Id, UBYTE Value)
{
	UWORD16 _Val = Value;
	// Alarmes non techniques
	if (Id < end_of_alarms_table)
	{
		_Val &= 0x00FF;
		DB_AlarmStatus[Id] &= 0x00FF;
		DB_AlarmStatus[Id] |= (_Val << 8);
	}
}
